public class PrimitivosBoolean {
    public static void main(String[] args) {
        boolean dataLogico = true;
        System.out.println("dataLogico = " + dataLogico);

        int n1 = 20;
        System.out.println("n1 = " + n1);
        int n2 = 10;
        System.out.println("n2 = " + n2);

        dataLogico = n1 < n2;
        System.out.println("dataLogico = " + dataLogico);

    }
}
